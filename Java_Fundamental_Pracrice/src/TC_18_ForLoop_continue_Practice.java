
public class TC_18_ForLoop_continue_Practice {

	public static void main(String[] args) {
		

		
		// Question: write a function which should print 1,2,3,5 using loop.
		
		

		for ( int i = 1; i <= 5; i ++)   
		  {
		   
		  if (i == 4) {   
		   continue;   
		  }  
		  
		  System.out.println(i);    
		      
		  }
	
		   
		    
		  }
	}


